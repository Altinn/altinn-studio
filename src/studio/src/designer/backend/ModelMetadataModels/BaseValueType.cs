        /// Time period type
        /// </summary>
        TimePeriod,

        /// <summary>
        /// Double type
        /// </summary>
        Double,
        
        /// <summary>
        /// Long type
        /// </summary>
        Long,

        /// <summary>
        /// Token type (derived from string)
        /// </summary>
        Token,

        /// <summary>
        /// Any uri type 
        /// </summary>
        AnyURI,

        /// <summary>
        /// Base64Binary type
        /// </summary>
        Base64Binary,

        /// <summary>
        /// Duration type
        /// </summary>
        Duration,
